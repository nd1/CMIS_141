/**
 * Variables Demonstration.
 */
public class MyVariables
{	
    public static void main(String[] args) 
     { 
        System.out.println("Hello, Variables!");
        // Declare and intialize 2 booleans
        boolean success = true;
        boolean failure = false;
        
        // Declare and intialize 2 doubles
       double avgAge = 45.2342;
       double avgIQ = 83.2221;
       
        // Declare and intialize 2 ints
       int age = 35;
       int IQ = 92;
        
         // Declare and intialize 2 chars
        char lowerC = 'c';
        char upperZ = 'Z';

        // Additional Hex and Binary int 
        int hexvalue = 0xf209;
        int binaryvalue = 0b0011;

        // Additional int with underscores
        int dollaramount = 4_000_000;
       
        // Print variable names and values
        System.out.println ("success = " + success);
        System.out.println ("failure = " + failure);
        System.out.println ("avgAge = " + avgAge);
        System.out.println ("avgIQ = " + avgIQ);
        System.out.println ("age = " + age);
        System.out.println ("IQ = " + IQ);
        System.out.println ("lowerC = " + lowerC);
        System.out.println ("hexvalue = " + hexvalue);
        System.out.println ("binaryvalue = " + binaryvalue);
        System.out.println ("dollaramount = " + dollaramount);
	
	// Experiment by removing semi-colons, changing variable names, adding more datatypes
        // And by changing values.
               
     }
}




